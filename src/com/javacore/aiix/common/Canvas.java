package com.javacore.aiix.common;
/**
 * Canvas - clear place for drawing
 * */
public abstract class Canvas {

    /**
     * Unimplemented method for drawing text
     * @param text Text to draw
     * */
    public abstract void drawText(String text);

    /**
     * Unimplemented method for drawing a square
     * @param x size of square
     * */
    public abstract void drawSquare(int x);
}
